							<h3>Lịch sử của Rolex</h3>
							<p>Alfred Davis và anh rể là Hans Wilsdorf đã sáng lập nên "Wilsdorf and Davis" tại London nước Anh vào năm 1905 [5], là công ty tiền thân mà sau này trở thành Rolex SA. Thời điểm ban đầu "Wilsdorf and Davis" hoạt động trong lĩnh vực lắp rắp đồng hồ bằng cách nhập khẩu những bộ máy đồng hồ Thụy Sỹ từ nhà sản xuất Hermann Aegler's đến Anh Quốc và kết hợp với những bộ vỏ đồng hồ chất lượng cao được sản xuất bởi Dennison và các nhà sản xuất khác. Những chiếc đồng hồ đầu tiên của "Wilsdorf and Davis" thường được gắn nhãn "W & D" ở mặt sau đồng hồ.</p>
							<p>Sau một thời gian hoạt động thành công, đến năm 1908[6], Wilsdorf đã đăng ký thương hiệu "Rolex" và mở một văn phòng tại La Chaux-de-Fonds, Thụy Sỹ. Đến 15 tháng 11 năm 1915, Rolex chính thức trở thành tên công ty, và được sử dụng như một thương hiệu đồng hồ cho đến tận sau này.</p>
							<p>Năm 1919, do chiến tranh thế giới thứ nhất và các loại thuế nhập khẩu và xuất khẩu quá cao[8] đánh vào các sản phẩm cao cấp xa xỉ sang trọng cũng như các sản phẩm được làm từ vàng và bạc, Wilsdorf đã rời nước Anh và chuyển toàn bộ công ty cũng như các cơ sở sản xuất tới Geneva Thụy Sỹ, nơi Rolex được thành lập trở thành công ty đồng hồ Rolex. </p>
							<p>Đến tháng 12 năm 2008, sau sự ra đi đột ngột của giám đốc điều hành Patrick Heiniger, công ty Rolex đã rơi vào sự khủng hoảng với việc hao hụt đến 1 tỷ franc Thụy Sĩ (xấp xỉ 574 triệu Bảng Anh, hay 900 triệu USD) vốn đầu từ với Bernard Madoff, người quản lý quỹ đầu tư người Mỹ đã bị kết tội hoạt động gian lận khoảng 30 tỷ Bảng Anh trên toàn thế giới</p>
